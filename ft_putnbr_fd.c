#include "libft.h"

void ft_putnbr_fd(int n, int fd)
{
        if (n == -2147483648)
        {
            write(fd, "-2147483648", 11);
        }
        if (n == 2147483647)
        {
            write(fd, "2147483647", 10);
        }
        else if (n < 0 && n >= -2147483647 )
        {
            ft_putchar_fd('-', fd);
            n *= -1;
            ft_putnbr_fd(n, fd);
        }
        else if (n > 9 && n < 2147483647 )
        {
            ft_putnbr_fd((n / 10) , fd);
            ft_putnbr_fd((n % 10) , fd);
        }
        else if (n >= 0 && n <= 9)
        {
            ft_putchar_fd(n + 48, fd);
        }
}